<center><b>System Settings</b></center>
<hr>

<p><b>Blank screen on startup</b> - Enable/Disable a screen blanker at FPP startup.  If you are using a projector with your Pi to display video then you will want to enable this setting so that the text console does not show on your projector when you are not playing videos</p>

<hr>
Latitude/Longitude are optional but are used to determine sunrise/sunset times in the FPP Scheduler.  There are various ways to locate your Latitude and Longitude values including <a href='https://www.google.com/maps/'>Google Maps</a> and <a href='https://www.latlong.net/'>LatLong.net</a>.
